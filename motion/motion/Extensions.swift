import UIKit


// Custom Colors
extension UIColor {
    
    class func moBlueColor(_ a:CGFloat = 1) -> UIColor {
        return UIColor(red: 0.25, green: 0.5, blue: 0.85, alpha: a)
    }
    
    class func moPinkColor(_ a:CGFloat = 1) -> UIColor {
        return UIColor(red: 0.93, green: 0.0, blue: 0.33, alpha: a)
    }
}

